def catalogo_estandar():
    """→ lista (código, nombre) del PUC de fábrica, con los nombres que la app
    ya usa en sus roles (CUENTAS_ESTANDAR) mandando sobre el nombre 2650, y
    los códigos propios de la app agregados si no estaban (ej. 240801/240802)."""
    por_codigo = dict(_PUC_BASE)
    for codigo, nombre in CUENTAS_ESTANDAR.values():
        por_codigo[codigo] = nombre
    return sorted(por_codigo.items())


def sembrar_puc_estandar(empresa):
    """Carga el PUC de fábrica en la empresa sin pisar lo que ya tenga
    (reentrante, mismo espíritu que importar_puc). → cuántas se crearon."""
    from .models import CuentaPUC

    existentes = set(CuentaPUC.objects.de_empresa(empresa)
                     .values_list("codigo", flat=True))
    nuevas = [CuentaPUC(empresa=empresa, codigo=codigo, nombre=nombre)
              for codigo, nombre in catalogo_estandar()
              if codigo not in existentes]
    CuentaPUC.objects.bulk_create(nuevas)
    return len(nuevas)
